SET SCHEMA RPGPROCDOC;

CREATE OR REPLACE TABLE RPGProcDoc (
ProcDocName VARCHAR(128) NOT NULL,
CreationTimestamp FOR COLUMN CREATETS TIMESTAMP NOT NULL,
SequenceNumber FOR COLUMN SEQNO INTEGER NOT NULL,
ProcedureName FOR COLUMN PROCNAME VARCHAR(256) NOT NULL,
LabelName VARCHAR(128) NOT NULL,
LabelValue VARCHAR(512) NOT NULL,
DocSourceMember FOR COLUMN DOCSRCM VARCHAR(128) NOT NULL,
DocSourceFile FOR COLUMN DOCSRCF VARCHAR(128) NOT NULL,
DocLibrary VARCHAR(128) NOT NULL,
PRIMARY KEY (ProcDocName, CreationTimestamp, DocLibrary, DocSourceFile, ProcedureName, SequenceNumber));

LABEL ON TABLE RPGProcDoc IS 'RPGProcDoc - Procedure Documentation Lines';
